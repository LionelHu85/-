# include<iostream>
using namespace std;


int main() {
	//转义字符

	//换行符号 \n
	cout << "hello world\n";

	//反斜杠 \\
 
	cout << "\\" << endl;

	//水平制表符 \t  作用是可以整齐的输出数据

	
	cout << "aaaaa\thelloworld" << endl;
	cout << "aa\thelloworld" << endl;
	cout << "aaaa\thelloworld" << endl;

	system("pause");
	
	return 0;
}